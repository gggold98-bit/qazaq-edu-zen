
CREATE OR REPLACE FUNCTION public.update_updated_at_column()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

CREATE TABLE public.legal_documents (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  title text NOT NULL,
  doc_number text,
  category text,
  content text NOT NULL,
  added_by uuid REFERENCES auth.users(id) ON DELETE SET NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

ALTER TABLE public.legal_documents ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Anyone authenticated can read legal documents"
  ON public.legal_documents FOR SELECT TO authenticated USING (true);

CREATE POLICY "Authenticated users can insert legal documents"
  ON public.legal_documents FOR INSERT TO authenticated
  WITH CHECK (auth.uid() = added_by);

CREATE POLICY "Users can update their own legal documents"
  ON public.legal_documents FOR UPDATE TO authenticated
  USING (auth.uid() = added_by);

CREATE POLICY "Users can delete their own legal documents"
  ON public.legal_documents FOR DELETE TO authenticated
  USING (auth.uid() = added_by);

CREATE TRIGGER update_legal_documents_updated_at
  BEFORE UPDATE ON public.legal_documents
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

